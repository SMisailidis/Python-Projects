#Ως παραμέτρους (p και q), καταχωρήστε τον αριθμό που θέλετε
p=1000
q=1024
n=p*q

print("Μετατροπή του αριθμού p σε δυαδική μορφή:")
print (p)
print (p.binary(), len(p.binary()))

print("\nΜετατροπή του αριθμού q σε δυαδική μορφή:")
